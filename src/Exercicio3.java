public class Exercicio3 {
    //Converter kilômetros para milhas
    public static void main(String[] args) {

        System.out.println("Converter kilômetros para milhas");
        double kilometros = 1.5;
        double milhas = 1.609344;



        System.out.println("O valor recebido em kilômetros é 1.5.");
        System.out.println("O resultado em milhas é "+ kilometros/milhas);

    }

}
